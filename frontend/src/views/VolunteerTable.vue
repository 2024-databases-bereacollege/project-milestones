<template>
  <div>
    <data-table
      :edited-item="editedItem"
      @update:edited-item="updateEditedItem"
      @save="saveItem"
      :headers="tableHeaders"
      :items="volunteers"
      :default-item="defaultItem" 
      @edit-item="editItem($event.item, $event.index)"
      @delete-item="deleteItem($event.item, $event.index)"
      @delete-item-confirm="deleteItemConfirm($event)"
      @close="close"
      @close-delete="closeDelete"
      sort-by="VolunteerID"
      sort-order="asc"
    ></data-table>
  </div>
</template>

<script>
// default item above may be edited item instead
import axios from 'axios';
import DataTable from '@/components/DataTable.vue';

export default {
  components: {
    DataTable,
  },
  data() {
    return {
      tableHeaders: [
        { title: 'Volunteer ID', key: 'VolunteerID' },
        { title: 'First Name', key: 'FirstName' },
        { title: 'Last Name', key: 'LastName' },
        { title: 'Email', key: 'Email' },
        { title: 'Phone', key: 'Phone' },
        { title: 'Has Record Access', key: 'HasRecordAccess' },
        { title: 'Actions', key: 'actions', sortable: false },
      ],
      volunteers: [],
      editedIndex: -1,
      editedItem: {
        VolunteerID: '',
        FirstName: '',
        LastName: '',
        Email: '',
        Phone: '',
        HasRecordAccess: false,
      },
      defaultItem: {
        VolunteerID: '',
        FirstName: '',
        LastName: '',
        Email: '',
        Phone: '',
        HasRecordAccess: false,
      },
    };
  },
  created() {
    this.fetchData();
  },
  methods: {
    fetchData() {
      axios.get('http://127.0.0.1:5000/api/volunteers')
        .then(response => {
          this.volunteers = response.data;
        })
        .catch(error => {
          console.error('Error fetching data:', error);
        });
    },
    editItem(item, index) {
    this.editedIndex = index;
    this.editedItem = Object.assign({}, item);
    },
    deleteItem(item, index) {
      this.editedIndex = index;
      this.editedItem = Object.assign({}, item);
    },
    deleteItemConfirm(index) {
      // Perform the delete operation in the database
      // Remove the item from the volunteers array using the index
      this.volunteers.splice(index, 1);
      this.closeDelete();
    },
    close() {
      this.editedIndex = -1;
      this.editedItem = Object.assign({}, this.defaultItem);
    },
    closeDelete() {
      this.editedIndex = -1;
      this.editedItem = Object.assign({}, this.defaultItem);
    },
    updateEditedItem(item) {
      this.editedItem = item;
    },
    saveItem(item) {
      if (this.editedIndex > -1) {
        // Perform the update operation in the database
        Object.assign(this.volunteers[this.editedIndex], item);
      } else {
        // Perform the create operation in the database
        this.volunteers.push(item);
      }
      this.close();
    },
  },
};
</script>
